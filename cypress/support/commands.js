Cypress.Commands.add('token', (usuario, senha) => {

    cy.request({

        method: 'POST',

        url: '/v1/auth',

        body: {

            email: usuario,

            senha: senha

        }

    }).its('body.data.token').should('not.be.empty')

        .then(tokenAdministrador => {

            return tokenAdministrador

        })

})
